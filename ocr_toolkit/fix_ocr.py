# ------------------------------------------------------------
# 正規表現によるパターン置換(置換結果が明確なもののみ)
# ------------------------------------------------------------
_RE_RULES: list[tuple[re.Pattern, str]] = [
    # カタカナ+「一」→「ー」(長音符の誤認識)
    (re.compile(r"([ァ-ヴ])一"), r"\1ー"),

    # 書籍 OCR 末尾に混入するページ番号ノイズ
    #   例: "…49 =" / "…P27" / "…117"
    (re.compile(r"\s*[…・]\s*\d{1,4}\s*[=＝]\s*$", re.MULTILINE), ""),
    (re.compile(r"\s*\*P?\d{1,4}\s*$", re.MULTILINE), ""),

    # 「星座*P27」のような書籍内参照ノイズ (配列要素末尾などに混入)
    (re.compile(r"[*＊]P?\d{1,4}"), ""),

    # 連続する空白・全角空白の圧縮 (行内のみ)
    (re.compile(r"[ \u3000]{2,}"), " "),

    # 3連続以上の改行を2に圧縮 (段落保持)
    (re.compile(r"\n{3,}"), "\n\n"),

    # 「ーダー」前のリ脱字復元 (リーダー / リーダーシップ系)
    # 直前がコ/カ/ヘ等でないとき (これらは別語の可能性) のみ
    (re.compile(r"(?<![コカヘホヤワッ])([、。「」(\s])ーダー"), r"\1リーダー"),

    # 「ーダーシップ」のヤ/ツ揺れ修正
    (re.compile(r"ーダー[シツ]ップ"), "ーダーシップ"),

    # 同字 3 連以上を 1 字に (二重 OCR の典型)
    # ※ "ーー" 等の長音は別ルールで処理するため除外
    (re.compile(r"([ァ-ヴ一-鿿])\1\1+"), r"\1"),

    # ひらがな + 5/3/8 + ひらがな → う/さ/お (OCR 数字誤認)
    # 例: "よ5に" → "ように", "そ5して" → "そうして"
    (re.compile(r"([ぁ-ん])5([ぁ-ん])"), r"\1う\2"),
]


