def fix_ocr(text: str, extra_fixes: Mapping[str, str] | None = None) -> str:
    """OCR結果の誤字を置換した文字列を返す。

    Args:
        text: OCR出力テキスト
        extra_fixes: プロジェクト固有の置換辞書(追加)
    """
    if not text:
        return ""
    # 1) プロジェクト固有辞書(優先)
    if extra_fixes:
        for wrong, right in extra_fixes.items():
            text = text.replace(wrong, right)
    # 2) 共通辞書
    for wrong, right in OCR_FIXES.items():
        text = text.replace(wrong, right)
    # 3) 正規表現ルール
    for pat, repl in _RE_RULES:
        text = pat.sub(repl, text)
    # 4) 促音・拗音の一般パターン
    for k, v in _TSUMAKU.items():
        text = text.replace(k, v)
    for k, v in _YOUON.items():
        text = text.replace(k, v)
    return text
